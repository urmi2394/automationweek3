package com.automationweek3;

public class WhileEvenOdd {
    public static void main(String[] args) {
        int u=2;
        int s=1;
        while (u<=10){
            System.out.println(u);
            u=u+2;
        }
        while (s<=10){
            System.out.println(s);
            s=s+2;
        }
    }
}
